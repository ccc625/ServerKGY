package kgy.search.vo;

public class BoardList {
	private String boardNo;
	private String boardUpper;
	private String teamNo;
	private String boardSeq;
	private String boardComment;
	private String boardRegTime;
	private String lvl;
	
	public String getBoardNo() {
		return boardNo;
	}
	public void setBoardNo(String boardNo) {
		this.boardNo = boardNo;
	}
	public String getBoardUpper() {
		return boardUpper;
	}
	public void setBoardUpper(String boardUpper) {
		this.boardUpper = boardUpper;
	}
	public String getTeamNo() {
		return teamNo;
	}
	public void setTeamNo(String teamNo) {
		this.teamNo = teamNo;
	}
	public String getBoardSeq() {
		return boardSeq;
	}
	public void setBoardSeq(String boardSeq) {
		this.boardSeq = boardSeq;
	}
	public String getBoardComment() {
		return boardComment;
	}
	public void setBoardComment(String boardComment) {
		this.boardComment = boardComment;
	}
	public String getBoardRegTime() {
		return boardRegTime;
	}
	public void setBoardRegTime(String boardRegTime) {
		this.boardRegTime = boardRegTime;
	}
	public String getLvl() {
		return lvl;
	}
	public void setLvl(String lvl) {
		this.lvl = lvl;
	}
	
	
}
