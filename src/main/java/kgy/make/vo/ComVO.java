package kgy.make.vo;

public class ComVO {
	
	private String common_cd;
	private String common_nm;
	private String common_seq;
	private String common_upper;
	
	public String getCommon_cd() {
		return common_cd;
	}
	public void setCommon_cd(String common_cd) {
		this.common_cd = common_cd;
	}
	public String getCommon_nm() {
		return common_nm;
	}
	public void setCommon_nm(String common_nm) {
		this.common_nm = common_nm;
	}
	public String getCommon_seq() {
		return common_seq;
	}
	public void setCommon_seq(String common_seq) {
		this.common_seq = common_seq;
	}
	public String getCommon_upper() {
		return common_upper;
	}
	public void setCommon_upper(String common_upper) {
		this.common_upper = common_upper;
	}
	
	
}
